export default interface DemoDragonTypes {
    /**
     * DemoDragon title
     * This will show a title in a container
     *
     * @example
     * title: 'My beautiful title'
     *
     */
    title: string;

    /**
     * DemoDragon Sub title
     * This will show a sub title in a container
     *
     * @example
     * subtitle: 'My beautiful sub title'
     *
     */
    subtitle?: string | undefined;
}
